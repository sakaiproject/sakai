
    create table lesson_builder_comments (
        id bigint generated by default as identity (start with 1),
        itemId bigint not null,
        pageId bigint not null,
        timePosted timestamp not null,
        author varchar(36) not null,
        commenttext longvarchar,
        UUID varchar(36) not null,
        html bit not null,
        points double,
        primary key (id)
    );

    create table lesson_builder_groups (
        id bigint generated by default as identity (start with 1),
        itemId varchar(255) not null,
        groupId varchar(255) not null,
        groups varchar(500),
	siteId varchar(250);
        primary key (id)
    );

    create table lesson_builder_items (
        id bigint generated by default as identity (start with 1),
        pageId bigint not null,
        sequence integer not null,
        type integer not null,
        sakaiId varchar(250),
        name varchar(100),
        html longvarchar,
        description varchar(500),
        height varchar(8),
        width varchar(8),
        alt varchar(500),
        nextPage bit,
        format varchar(255),
        required bit,
        alternate bit,
        prerequisite bit,
        subrequirement bit,
        requirementText varchar(20),
        sameWindow bit,
        groups varchar(500),
        anonymous bit,
        showComments bit,
        forcedCommentsAnonymous bit,
        gradebookId varchar(35),
        gradebookPoints integer,
        gradebookTitle varchar(200),
        altGradebook varchar(35),
        altPoints integer,
        altGradebookTitle varchar(200),
        primary key (id)
    );

    create table lesson_builder_log (
        id bigint generated by default as identity (start with 1),
        lastViewed timestamp not null,
        itemId bigint not null,
        userId varchar(255) not null,
        firstViewed timestamp not null,
        complete bit not null,
        dummy bit not null,
        path varchar(255),
        toolId varchar(250),
        studentPageId bigint,
        primary key (id)
    );

    create table lesson_builder_pages (
        pageId bigint generated by default as identity (start with 1),
        toolId varchar(250) not null,
        siteId varchar(250) not null,
        title varchar(100) not null,
        parent bigint,
        topParent bigint,
        hidden bit,
        releaseDate timestamp,
        gradebookPoints double,
        owner varchar(36),
        groupOwned bit,
        cssSheet varchar(250),
        primary key (pageId)
    );

    create table lesson_builder_student_pages (
        id bigint generated by default as identity (start with 1),
        lastUpdated timestamp not null,
        itemId bigint not null,
        pageId bigint not null,
        title varchar(100) not null,
        owner varchar(36) not null,
        groupOwned bit not null,
        commentsSection bigint,
        lastCommentChange timestamp,
        deleted bit,
        points double,
        primary key (id)
    );

    create table lesson_builder_properties (
        id bigint generated by default as identity (start with 1),
        attribute varchar(255) not null,
        value longvarchar,
        primary key (id),
        unique (attribute)
    );
